from imu_reg_conversion import convert_imu_register_value

def test_convert_imu_register_value_negative_low():
    assert convert_imu_register_value(1, 0, 54) == -54

def test_convert_imu_register_value_positive_low():
    assert convert_imu_register_value(0, 0, 243) == 243

def test_convert_imu_register_value_positive_high():
    assert convert_imu_register_value(0, 1, 86) == 342

def test_convert_imu_register_value_negative_high():
    assert convert_imu_register_value(1, 1, 86) == -342
